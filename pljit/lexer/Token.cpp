#include "Token.hpp"
//---------------------------------------------------------------------------
namespace pljit::lexer {
// --------------------------------------------------------------------------
Token::Token() : m_source(nullptr, 0, 0), m_type(Type::undefined)
{

}
// --------------------------------------------------------------------------
Token::Token(code::Range source, Token::Type type)
        : m_source(source), m_type(type)
{

}
// --------------------------------------------------------------------------
Token::Type Token::type() const
{
    return m_type;
}

// --------------------------------------------------------------------------
code::Range Token::range() const
{
    return m_source;
}
//---------------------------------------------------------------------------
std::string_view Token::view() const
{
    return m_source.view();
}
// --------------------------------------------------------------------------
} // pljit::lexer
